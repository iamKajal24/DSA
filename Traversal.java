public class Traversal {

	public static void main(String[] args) {
		int arr[] = { 43, 87, 56, 34, 98, 72 };
		System.out.println("Element of Array : ");
		for (int i = 0; i < arr.length; i++) {
			System.out.println("Arr " + i + " = " + arr[i] + " ");
		}
	}
}
